/**
 * Esta classe soma dois números inteiros
 * @author Guilherme Medeiros
 * */


public class SomaInteiros {

    /**
     * Método que realiza a soma de dois números
     * soma = a + b
     * @param a é o operando 1
     * @param b é o operando 2
     * @return o resultado da soma dos operandos 1 e 2.
     */
    public int soma(int a, int b){
        return a+b;
    }

    /**
     * Método que faz a soma de 3 operandos:
     * @param a operando 1
     * @param b operando 2
     * @param c operando 3
     * @return operando 1 + operando 2 + operando 3
     */

    public int soma3(int a, int b, int c){
        return a+b+c;
    }



}
